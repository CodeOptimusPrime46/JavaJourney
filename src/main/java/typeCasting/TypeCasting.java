package typeCasting;

public class TypeCasting {

    public static void main(String args[]){

        //Automatic type casting
        int x = 10;
        float y = x;
        System.out.println("float number is " + y);

        //Manual type casting
        double a = 19.5423;
        int number  = (int)a;
        System.out.println("Integer number is " + number);


    }
}
